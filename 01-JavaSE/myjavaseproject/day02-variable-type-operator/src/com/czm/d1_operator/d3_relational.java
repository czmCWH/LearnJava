package com.czm.d1_operator;

public class d3_relational {
    public static void main(String[] args) {
        /*
         1、关系运算符
         >，a > b，判断 a 是否大于 b；
         >=，a >= b，判断 a 是否大于或等于 b；
         <，a < b，判断 a 是否小于 b；
         <=，a <= b，判断 a 是否小于或等于 b；
         ==，a == b，判断 a 是否等于 b；
         !=，a != b，判断 a 是否不等于 b；

         判断数据是否满足条件，最终会返回一个判断的结果，这个结果是布尔类型的值:true或者false。
         */
        int a = 10;
        int b = 5;
        boolean res = a > b;
        System.out.println(res);

    }
}
